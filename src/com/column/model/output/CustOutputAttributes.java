package com.column.model.output;

public class CustOutputAttributes {

	private String outputAttributeName;
	private String outputAttributeValue;
	public String getOutputAttributeName() {
		return outputAttributeName;
	}
	public void setOutputAttributeName(String outputAttributeName) {
		this.outputAttributeName = outputAttributeName;
	}
	public String getOutputAttributeValue() {
		return outputAttributeValue;
	}
	public void setOutputAttributeValue(String outputAttributeValue) {
		this.outputAttributeValue = outputAttributeValue;
	}
	
	
}
